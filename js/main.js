// Основные функции JavaScript

// Добавление обработчиков событий для всех ссылок в навигации

// Обработка кликов по навигации
// Получение всех ссылок в навигации и перебор их
document.querySelectorAll('nav a').forEach(link => {
  // Добавление обработчика события клика для каждой ссылки
  link.addEventListener('click', function(e) {
    // Отмена стандартного поведения ссылки
    e.preventDefault();
    // Плавная прокрутка к целевому разделу страницы
    // Получение целевого элемента по атрибуту href и прокрутка к нему
    document.querySelector(this.getAttribute('href')).scrollIntoView({
      // Настройка плавной анимации прокрутки
      behavior: 'smooth'
    }); // Конец перебора ссылок в навигации // Конец обработчика события клика // Конец настройки прокрутки
  }); // Конец перебора ссылок в навигации // Конец обработчика события клика
}); // Конец перебора ссылок в навигации

// Динамическое обновление года в футере

// Получение элемента с годом в футере
const footerYear = document.querySelector('footer p'); // Поиск параграфа в футере
// Замена года в тексте на текущий год
footerYear.textContent = footerYear.textContent.replace('2025', new Date().getFullYear());